DROP TABLE RATING_FACE;

CREATE TABLE RATING_FACE(
RATING_FACE_NUMBER NUMBER PRIMARY KEY, /*번호*/
MOVIE_ID NUMBER REFERENCES MOVIE (MOVIE_ID), /*영화 ID*/
USER_ID VARCHAR2(40) REFERENCES USERS (USER_ID), /*유저 ID*/
RATING_FACE_value NUMBER CHECK (RATING_FACE_value IN(1,2))
/*1 : 좋아요  2:싫어요*/
);


